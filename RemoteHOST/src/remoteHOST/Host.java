package remoteHOST;

public class Host {
	private String Ip;
	private String NomeDoHost;
	private String TipodeConexao;
	public String getIp() {
		return Ip;
	}
	public void setIp(String ip) {
		Ip = ip;
	}
	public String getNomeDoHost() {
		return NomeDoHost;
	}
	public void setNomeDoHost(String nomeDoHost) {
		NomeDoHost = nomeDoHost;
	}
	public String getTipodeConexao() {
		return TipodeConexao;
	}
	public void setTipodeConexao(String tipodeConexao) {
		TipodeConexao = tipodeConexao;
	}
	
	

}
