package remoteHOST;

public class BancoDeDados {
	private String UsuarioDoBanco;
	private String SenhaDoBanco;
	private String NomeDoBanco;
	
	public String getUsuarioDoBanco() {
		return UsuarioDoBanco;
	}
	public void setUsuarioDoBanco(String usuarioDoBanco) {
		UsuarioDoBanco = usuarioDoBanco;
	}
	public String getSenhaDoBanco() {
		return SenhaDoBanco;
	}
	public void setSenhaDoBanco(String senhaDoBanco) {
		SenhaDoBanco = senhaDoBanco;
	}
	public String getNomeDoBanco() {
		return NomeDoBanco;
	}
	public void setNomeDoBanco(String nomeDoBanco) {
		NomeDoBanco = nomeDoBanco;
	}

}
